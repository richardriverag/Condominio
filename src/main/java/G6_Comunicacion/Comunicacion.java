    private void cargarDatos2() {
        Conexion conexion = new Conexion();

        try {
            String sSQl = "SELECT * FROM Usuario";
            Connection con = conexion.getCon();
            Statement cn = con.createStatement();
            ResultSet rn = cn.executeQuery(sSQl);

            while (rn.next()) {
                UsuarioAnuncios.addItem(rn.getString("usuario"));
            }

            // Cerrar recursos
            rn.close();
            cn.close();
            con.close();
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    private void cargarDatos() {
        model1.addColumn("ASUNTO");
        model1.addColumn("USUARIO");
        model1.addColumn("TIPO USUARIO");
        model1.addColumn("DESCRIPCION");
        try {
            String sSQL = "SELECT * FROM Comunicacion";
            Connection con = conexion.getCon();
            Statement cn = con.createStatement();
            ResultSet rn = cn.executeQuery(sSQL);
            while (rn.next()) {
                model1.addRow(new Object[]{rn.getString("asunto"), rn.getString("usuario"), rn.getInt("tipoUsuario"), rn.getString("descripcion")});
            }
            jTableAnuncios.setModel(model1);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

