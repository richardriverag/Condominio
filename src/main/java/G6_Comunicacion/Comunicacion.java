            Statement cn = con.createStatement();
            int agregar = cn.executeUpdate(sSQl);
            cn.close();
            JOptionPane.showMessageDialog(null, "Los datos se han insertado correctamente.", "Inserción Exitosa", JOptionPane.INFORMATION_MESSAGE);
