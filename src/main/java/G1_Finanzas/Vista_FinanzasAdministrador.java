    private void jTTipoPagoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTTipoPagoMousePressed
        PreparedStatement ps;
        ResultSet rs;
        try {
            int Fila = jTTipoPago.getSelectedRow();
            String idTPago = jTTipoPago.getValueAt(Fila, 0).toString();
            ps = cn.prepareStatement("SELECT ID_METODOPAGO, NOMBRE_METODOPAGO FROM METODOPAGO WHERE ID_METODOPAGO =?");
            ps.setString(1, idTPago);
            rs = ps.executeQuery();
            if(jPModificarTipoPago.isShowing()){
                while (rs.next()) {
                    this.jTFIdTipoPagoM.setText(rs.getString("ID_METODOPAGO"));
                    this.jTFNombrePagoM.setText(rs.getString("NOMBRE_METODOPAGO"));
                }
            }
            if(jPEliminarTipoPago.isShowing()){
                while (rs.next()) {
                    this.jTFIdTipoPagoE.setText(rs.getString("ID_METODOPAGO"));
                    this.jTFNombrePagoE.setText(rs.getString("NOMBRE_METODOPAGO"));
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jTTipoPagoMousePressed

    private void jBRegistrarIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistrarIngresoActionPerformed
        
         // boton de registrar 
        String usuario1 = this.jTFCodigoUsuarioRegistroIngreso.getText();
        String formapago1 = this.cmbPagoRegistrarIngreso.getSelectedItem().toString();
        //montoPago = Float.parseFloat(this.jTFValorRegistrarIngreso.getText()); //utilizo el que esta en el evento
        //String fecha1 = fechaActualPago.toString();
        
        LocalDate fechaActualPago = LocalDate.now();
        // Sumar n meses (numeroCuotas) a la fecha actual
        LocalDate fechaVencimientoPago = fechaActualPago.plusMonths(numeroCuotas);
        
        
        /*--------------------------------------------*/
               
        //(String id_Pagos, String id_Usuario, Date fecha_Pago, String id_MetodoPago, float monto_Pago, TipoPago tipoPago_Pago, Date fechaVencimiento_Pago, String estado_Pago)
        TipoPago tipoPagoRegistrarIngreso = new TipoPago("",formapago1);
        Pago pagoIngreso = new Pago("",usuario1,fechaActualPago.toString(),formapago1,montoPago,tipoPagoRegistrarIngreso,fechaVencimientoPago.toString(),"Vigente");
        metodosSQLPago.insertarPagoIngresoEgreso(cn, pagoIngreso, true, false);
        metodosSQLPago.desplegarIngresosEgresos(cn, this.jTableRegistrarIngreso, true, false);

        
        /*--------------------------------------------*/
        //---------------------Registrar cuotas-------------------
       
        List<Cuota> listaCuotas = new ArrayList<>();
        // Utilizando un bucle for para crear m√∫ltiples objetos Cuota y agregarlos a la lista
        for (int i = 1; i <= numeroCuotas; i++) {
            // Sumar i meses a la fecha actual y agregarla a la lista de fechas
            LocalDate fechaVencimientoCuota = fechaActualPago.plusMonths(i);
            // Crear una nueva cuota con el n√∫mero de cuota, monto y fecha de vencimiento
            Cuota cuota = new Cuota(i, montoCuota, fechaVencimientoCuota.toString());
            listaCuotas.add(cuota);
        }
        
        metodosSQLCuota.agregarCuotas(cn,listaCuotas,numeroCuotas);
        metodosSQLCuota.desplegarCuotas(cn, jTPruebaCuotas);
        
    }//GEN-LAST:event_jBRegistrarIngresoActionPerformed

    private void jCBNumeroCuotasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCBNumeroCuotasItemStateChanged
        //Calcular el monto a pagar en cada cuota
        montoPago = Float.parseFloat(this.jTFValorRegistrarIngreso.getText());
        numeroCuotas = Integer.parseInt((String)jCBNumeroCuotas.getSelectedItem());
        interesCuota = 1 + Float.parseFloat((String) this.jComboBoxInteresRegistroIngreso.getSelectedItem()) / 100;
        double montoCuota = montoPago / numeroCuotas * interesCuota;
        BigDecimal bd = new BigDecimal(montoCuota);
        bd = bd.setScale(2, RoundingMode.HALF_UP);
        montoCuota = bd.doubleValue();        
        jLMontoCuota.setText(String.format(montoCuota+""));
        
    }//GEN-LAST:event_jCBNumeroCuotasItemStateChanged

